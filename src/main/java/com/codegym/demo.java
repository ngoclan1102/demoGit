package com.codegym;

public class demo {
    private int id;
    private String name;

    public demo(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public demo() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
